     public boolean ValidateCardNumber(){
		if(cardNumberText.getText().length() != 16 && creditRadBtn.isSelected()){
			JOptionPane.showMessageDialog(this, "Invalid Card Number."+ "\n"+ "Please check again.");	
			return false;
		}
		else{
		return true;
		}
	}
	//it gives an exception if nothing was entered in the field "NumberFormatException"
	public boolean ValidateExpiryYear(){
		int exYear = Integer.parseInt(exYearText.getText());
		//no max expiry year
		try{
		if (exYear < 2022){
			JOptionPane.showMessageDialog(this, "Invalid Expiry date.");
			return false;
		}
		else{return true;}
		}catch(NumberFormatException nfe){
			System.out.println(nfe.getMessage());
		}
		return false;
	}
	
	public boolean ValidateSecurityCode(){
		String securityCode = new String(secCodeText.getPassword());
		if (securityCode.length() != 3){
			JOptionPane.showMessageDialog(this, "Invalid Security Code. Please check again.");
			return false;
		} 
		else{return true;}
	}
	
	
    private void btn_confirmActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btn_confirmActionPerformed
        // TODO add your handling code here:
        String oldPass, newPass, confrimPass;
        oldPass = txt_oldpass.getText();
        newPass = txt_newpass.getText();
        confrimPass = txt_confirm.getText();
        if( !oldPass.equalsIgnoreCase(user.getPass()) ){
            JOptionPane.showMessageDialog(this, "You are entering wrong password, it may be a wrong user", "Error", JOptionPane.ERROR_MESSAGE);
            txt_oldpass.setText("");
            txt_newpass.setText("");
            txt_confirm.setText("");
        }else if(newPass.length() < 8){
            JOptionPane.showMessageDialog(this, "The max size of password is 8 or more", "Error", JOptionPane.ERROR_MESSAGE);
            txt_newpass.setText("");
            txt_confirm.setText("");
        }
        else if( !newPass.equalsIgnoreCase(confrimPass)){
            JOptionPane.showMessageDialog(this, "Confirm password is wrong", "Error", JOptionPane.ERROR_MESSAGE);
            txt_confirm.setText("");      
        }
        else{
            user.setPass(newPass);
            updatePass(user, newPass);
            txt_oldpass.setText("");
            txt_confirm.setText("");
        }
    }//GEN-LAST:event_btn_confirmActionPerformed

