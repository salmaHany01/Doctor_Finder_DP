    private void uploadData(User user){
        lbl_fname.setText(user.getName());        
        lbl_lname.setText(user.getLname());
        if(user.getGender().equalsIgnoreCase("male")){
            r_male.setSelected(true);
        }
        else{
            r_female.setSelected(true);
        }
        lbl_add.setText(user.getAddress());
        lbl_phone1.setText( Integer.toString(user.getPhoneNo()) );
        lbl_phone2.setText(Integer.toString(user.getPhoneNo2()));
        lbl_email.setText(user.getEmail());
        lbl_email.enableInputMethods(false);
    }
    
    
    private void updateDataDB(User u){
        try{
                Statement updateStat = con.createStatement();
		String sql = "update user1 set fname = '"+ u.getName()+"', lname = '"+ u.getLname()+"' , gender = '"+ u.getGender()+"', phoneno1 = "+ u.getPhoneNo()+", phoneno2 = "+ u.getPhoneNo2()+", address = '"+ u.getAddress()+"', email = '"+ u.getEmail()+"' where id = "+ UserLogin.userID +" ";
                updateStat.executeUpdate(sql);
                JOptionPane.showMessageDialog(this, "Updated Successfully");
        }catch(Exception e){
            JOptionPane.showMessageDialog(this, "Update database " + e.toString());
        }
    }
    
